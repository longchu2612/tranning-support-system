    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        List<Setting> listSetting = new WebContactDAO().getAllContactCategory();
        int id = Integer.parseInt(request.getParameter("sid"));
        User user = dao.getSupporterByID(id);
        request.setAttribute("user", user);
        request.setAttribute("listSetting", listSetting);
        request.getRequestDispatcher("contactdetail.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        int supporter_id = Integer.parseInt(request.getParameter("sid"));
        int category_id = Integer.parseInt(request.getParameter("category"));
        String name = request.getParameter("name");
        String mobile = request.getParameter("phone");
        String email = request.getParameter("email");
        String mess = request.getParameter("message");
        WebContact webContact = new WebContact(-1, category_id, supporter_id, name, email, mobile, mess);
        dao.saveWebContact(webContact);
        //response.sendRedirect("home.jsp");

    }

